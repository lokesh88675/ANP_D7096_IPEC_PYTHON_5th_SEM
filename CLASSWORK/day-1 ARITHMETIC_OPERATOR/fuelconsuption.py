#Write a Python program to find the average mileage of a car
Total_distance_traveled = float(input("enter distance traveled (in KM)")) 
Fuel_consumed =   float(input("enter FUEL CONSUMPTION (in LITERS)")) 
 # THE AVERAGE IS GIVEN BELOW IN KM\L
Mileage = Total_distance_traveled / Fuel_consumed
print("the Mileage is ",Mileage)
